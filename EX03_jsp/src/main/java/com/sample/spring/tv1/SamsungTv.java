package com.sample.spring.tv1;

public class SamsungTv {
	public SamsungTv() {
		System.out.println("--->SamsungTv");
	}
	
	public void turnOn() {
		System.out.println("SamsungTv turn on");
	}
	
	public void turnOff() {
		System.out.println("SamsungTv turn off");
	}
	
	public void soundOn() {
		System.out.println("SamsungTv sound on");
	}
	
	public void soundOff() {
		System.out.println("SamsungTv sound off");
	}
}
